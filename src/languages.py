def get_language_display(code: str) -> str:
    """Get display string for a language code."""
    for lang in SUPPORTED_LANGUAGES:
        if lang['code'] == code:
            return f"{lang['name']}"
    return code

def get_language_code(display: str) -> str:
    """Extract language code from display string."""
    for lang in SUPPORTED_LANGUAGES:
        display_str = f"{lang['name']}"
        if display_str == display:
            return lang['code']
    return "en-US"  # Default
